import json
import numpy as np
import torch
from torch.utils.data import Dataset


class TrackingClipDataset(Dataset):
    """
    PyTorch Dataset for loading tracking clips with features and masks.
    
    Loads JSON metadata and corresponding .npy files generated by create_tracking_clips.py.
    Each sample consists of:
    - features: (T=150, N=23, F=5) numpy array
    - mask: (T=150, N=23) boolean numpy array
    - label: action label string
    - match_id: identifier for the match
    - game_time: timestamp in the match
    """
    
    def __init__(self, json_path: str):
        """
        Initialize the dataset by loading JSON metadata.
        
        Args:
            json_path (str): Path to JSON file containing metadata.
                            Each entry should have keys: 'game_time', 'action', 
                            'match_id', 'npy_path', 'mask_path', 'center_frame'
        """
        with open(json_path) as f:
            self.data = json.load(f)
    
    def __len__(self) -> int:
        """Return the total number of samples in the dataset."""
        return len(self.data)
    
    def __getitem__(self, idx: int) -> tuple:
        """
        Get a single sample from the dataset.
        
        Args:
            idx (int): Index of the sample to retrieve.
        
        Returns:
            tuple: (features_tensor, mask_tensor, label, match_id, game_time)
                - features_tensor: torch.FloatTensor of shape (T=150, N=23, F=5)
                - mask_tensor: torch.BoolTensor of shape (T=150, N=23)
                - label: str, action label
                - match_id: str, match identifier
                - game_time: str, game time
        """
        entry = self.data[idx]
        
        # Load features from npy file
        features = np.load(entry['npy_path'])
        features_tensor = torch.FloatTensor(features)
        
        # Load mask from npy file
        mask = np.load(entry['mask_path'])
        mask_tensor = torch.BoolTensor(mask)
        
        # Extract metadata
        label = entry.get('action', '')
        match_id = entry.get('match_id', '')
        game_time = entry.get('game_time', '')
        
        return features_tensor, mask_tensor, label, match_id, game_time
